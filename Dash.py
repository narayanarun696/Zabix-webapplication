def dategraph(graphdate,df):
    return  html.Div([
    dcc.DatePickerRange(
        id=graphdate,
        start_date=df['Date'].min(),
        end_date=df['Date'].max(),
        show_outside_days=True,
        day_size=32,
        display_format='DD/MM/YYYY',
    )])




def drawTable(table, df):
    return html.Div([
        dash_table.DataTable(
            id = table,
            editable=False,
            sort_action="native",
            sort_mode="multi",
            filter_action='native',
            page_action='native',
            page_current= 0,
            page_size=25,   
            data=df.to_dict('records'),
            columns=[{'id': c, 'name': c} for c in df.columns],
            style_header={
                'color': 'black',
                'border': '2px solid black',
                'backgroundColor': 'rgb(220, 220, 220)',   
                'padding':'10px',
            },
            style_data={
                'backgroundColor': 'rgb(50, 50, 50)',
                'color': 'white',
                'border': '2px solid black',
                'whiteSpace': 'normal',
                'height': 'auto',
            },   
            style_table={
                'overflow':'scroll',
                'height':550},
            
            style_cell={
                'textAlign':'center',
                'minWidth': 100, 
                'maxWidth': 100, 
                'width': 100,
                'font_size': '20px',
                'whiteSpace':'normal',
                'height':'auto'},
        )
    ])



